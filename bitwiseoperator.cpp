#include<iostream>
using namespace std;

int main(){
	int a=6;
	int b=4;
	
	cout<<"a&b"<<(a&b) << endl;
	cout<<"a|b"<<(a|b) << endl;
	cout<<"~a"<<(~a) << endl;
	cout<<"a^b"<<(a^b) << endl;
	
	int i=7;
	cout<< i++<<endl;
	cout<< ++i<<endl;
	cout<< --i<<endl;
	cout<< i-- << endl;
	
}
